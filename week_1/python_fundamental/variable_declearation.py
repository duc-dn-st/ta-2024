# 変数を定義する例
"""
変数 = 値
"""
# 変数名は任意の名前をつけることができる
x = 10
name = "Turtlebot"
pi = 3.14159
list = [1, 2, 3, 4, 5]
tuple = (1, 2, 3, 4, 5)

# 変数をTerminalに表示する
print(x)
# >> 10
print(name)
# >> Turtlebot
print(pi)
# >> 3.14159
print(list)
# >> [1, 2, 3, 4, 5]
print(tuple)
# >> (1, 2, 3, 4, 5)

