# operationsとは "操作" という意味です。
# このファイルには、Pythonの基本的な演算子についてのコードが書かれています。

# 1. 算術演算子
a = 4
b = 3
# + : 加算
kasan = a + b
print(kasan)

# - : 減算
genzan = a - b
print(genzan)

# * : 掛け算
kakezan = a * b
print(kakezan)

# / : 割り算
warizan = a / b
print(warizan)

# ** : べき乗
beki = a ** b
print(beki)

# // : 切り捨て除算
kirisute = a // b
print(kirisute)
